    <h3>7. Bridge Pose (Setu Bandhasana)</h3>
    <img src="bridge.gif" alt="Bridge Pose (Setu Bandhasana)">
    <p>The bridge pose helps to stretch the chest, neck, spine, and hips. It can help alleviate stress and mild depression by opening the heart and improving circulation.</p>
    <h2><strong>How to do it:</strong></h2>
    <p>1. Lie on your back with your knees bent and feet hip-width apart, flat on the floor.</p>
    <p>2. Press your feet and arms into the floor as you lift your hips towards the ceiling.</p>
    <p>3. Clasp your hands together underneath your pelvis and straighten your arms.</p>
    <p>4. Hold the pose for 30 seconds to 1 minute, breathing deeply.</p>
  </div>
<div class="meditation-card">
    <h3>8. Reclining Bound Angle Pose (Supta Baddha Konasana)</h3>
    <img src="reclined.gif" alt="Reclining Bound Angle Pose (Supta Baddha Konasana)">
    <p>The reclining bound angle pose is a relaxing pose that opens the hips and groin area, relieving tension and promoting relaxation.</p>
    <h2><strong>How to do it:</strong></h2>
    <p>1. Sit on the floor with your spine straight and legs extended in front of you.</p>
    <p>2. Bend your knees and bring the soles of your feet together, allowing your knees to drop toward the floor.</p>
    <p>3. Slowly recline backwards, supporting yourself with your elbows and forearms, until you are lying flat on your back.</p>
    <p>4. Relax your arms by your sides and close your eyes, breathing deeply.</p>
    <p>5. Hold the pose for 5-10 minutes, focusing on relaxation.</p>
  </div>
