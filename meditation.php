    <h3>1.Bow Pose (Dhanurasana)</h3>
    
    <p>The bow pose is a back-bend pose that stretches out and strengthens your back, stimulates your reproductive organs, and eases menstrual pain.</p>
    <h2><strong>How to do it:<strong></h2>
<img src="bow.gif" alt="Bow Pose (Dhanurasana) GIF">
    <p>1. Lie on your stomach with your arms alongside your body, palms up.</p>
    <p>2. Exhale and bend your knees, bringing your heels as close as possible to your buttocks.</p>
    <p>3. Reach back with your hands and take hold of your ankles.</p>
    <p>4. Inhale and lift your heels away from your buttocks, simultaneously lifting your thighs away from the floor.</p>
    <p>5. Hold the pose for 20-30 seconds, breathing easily.</p>
