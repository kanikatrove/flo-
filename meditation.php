    <img src="inverted.gif" alt="Inverted Leg Pose (Viparita Karani) GIF">
    <p>The inverted leg pose is a restorative pose that helps to calm the mind, relieve tired legs and feet, and reduce symptoms of mild depression.</p>
    <h2><strong>How to do it:</strong></h2>
    <p>1. Lie on your back with your legs extended up against a wall.</p>
    <p>2. Scoot your hips as close to the wall as possible, allowing your legs to rest against it.</p>
    <p>3. Rest your arms by your sides, palms facing up.</p>
    <p>4. Hold the pose for 5-15 minutes, breathing deeply.</p>
