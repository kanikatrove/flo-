  <div class="meditation-card">
    <h3>2.Camel Pose (Ustrasana)</h3>
        <p>The camel pose can get a little uncomfortable to perform during your periods, but the results are truly worth the effort. It involves stretching your abdomen, which will straighten out your uterine muscles and keep them from contracting.</p>
    <h2><strong>how to do it:</strong></h2>
<img src="camel.gif" alt="Camel Pose (Ustrasana)">
    <p>1. Kneel on the floor with your knees hip-width apart and thighs perpendicular to the floor.</p>
    <p>2. Press your shins and the tops of your feet firmly into the floor.</p>
    <p>3. Inhale and lift your chest towards the ceiling while drawing your shoulders back.</p>
    <p>4. Reach your hands back, one at a time, to grasp your heels.</p>
    <p>5. Hold the pose for 15-30 seconds, breathing deeply.</p>
  </div>

  <div class="meditation-card">
    <h3>3.Cobra Pose (Bhujangasana)</h3>
        <p>The cobra pose is good for relieving headaches that occur before or during menstrual periods. It does so by stimulating blood circulation. This backbend pose also helps strengthen and loosen the stiff muscles in your back and hips.</p>
    <h2><strong>How to do it:</strong></h2>
<img src="cobra.gif" alt="Cobra Pose (Bhujangasana)">

    <p>1. Lie prone on the floor, with your legs extended and the tops of your feet resting on the ground.</p>
    <p>2. Place your hands underneath your shoulders, elbows bent and close to your body.</p>
    <p>3. Inhale and lift your chest off the ground by straightening your arms. Keep your elbows slightly bent.</p>
    <p>4. Hold the pose for 15-30 seconds, breathing deeply.</p>
  </div>

  <div class="meditation-card">
    <h3>4.Butterfly Pose (Baddha Konasana)</h3>
       <p>The butterfly pose is a bound angle pose that employs the force of gravity to stimulate the downward flow of energy and blood to the groin. This helps relieve menstrual cramps and the associated fatigue.</p>
    <h2><strong>How to do it:</strong></h2>
 <img src="butterfly.gif" alt="Butterfly Pose (Baddha Konasana)">

    <p>1. Sit on the floor with your spine straight and legs extended in front of you.</p>
    <p>2. Bend your knees and bring the soles of your feet together, allowing your knees to drop toward the floor.</p>
    <p>3. Hold your feet with your hands, keeping your spine straight.</p>
    <p>4. Press your knees towards the floor gently, feeling a stretch in your inner thighs.</p>
    <p>5. Hold the pose for 30-60 seconds, breathing deeply.</p>
  </div>
<div class="meditation-card">
    <h3>5. Child's Pose (Balasana)</h3>
    <img src="childs.gif" alt="Child's Pose (Balasana)">
    <p>The child's pose is a restful pose that gently stretches your lower back, hips, thighs, and ankles. It's great for relaxing and relieving stress.</p>
    <h2><strong>How to do it:</strong></h2>
    <p>1. Kneel on the floor with your big toes touching and knees about hip-width apart.</p>
    <p>2. Sit back on your heels and stretch your arms forward, lowering your chest toward the ground.</p>
    <p>3. Rest your forehead on the floor and relax into the pose, breathing deeply.</p>
    <p>4. Hold the pose for 1-3 minutes, focusing on deep breathing.</p>
  </div>

 <div class="meditation-card">
    <h3>6. Corpse Pose (Savasana)</h3>
    <img src="corpse.gif" alt="Corpse Pose (Savasana)">
    <p>The corpse pose is a relaxation pose that helps calm the mind and reduce stress and fatigue. It's a great way to end your yoga practice.</p>
    <h2><strong>How to do it:</strong></h2>
    <p>1. Lie on your back with your legs extended and arms by your sides, palms facing up.</p>
    <p>2. Close your eyes and relax your entire body, letting go of any tension.</p>
    <p>3. Focus on your breath, allowing it to become slow and steady.</p>
    <p>4. Remain in this pose for 5-10 minutes, or longer if desired.</p>
  </div>

  

  
